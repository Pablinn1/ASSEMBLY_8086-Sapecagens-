
;Recebendo e printando numeros com 3 digitos

#ORG 100H

JMP START

VAR1 DB 3, 0 , 3 DUP '$') 


START:

MOV AL, 51

PART_NUMBER:

MOV BL, 0AH
DIV BL

MOV DH, AH ; SALVO O DIGITO MENOS SIGNIFICATIVO, QUE VARIA DE 0 A 9 EM HEXA
           ;OU SEJA, N TENHO NECESSIDADE DE TRASNFORMAR EM DECIMAL

XOR AH, AH ;  Caso AH contenha um numero > 0

PART_NUMBER_2:

DIV BL

MOV CH, AH

MOV CL, AL

PRINT_NUMBERS:

ADD DH, 30H
ADD CH, 30H
ADD CL, 30H

MOV DL, CL
MOV AH, 02H
INT 21H

MOV DL, CH
MOV AH, 02H
INT 21H

MOV DL, DH
MOV AH, 02H
INT 21H


END 